void main() {
  int a = 5;
  int b = 10;

  // Operasi perkalian
  int perkalian = a * b;
  print('$a * $b = $perkalian');

  // Operasi penjumlahan
  int penjumlahan = a + b;
  print('$a + $b = $penjumlahan');

  // Operasi pengurangan
  int pengurangan = a - b;
  print('$a - $b = $pengurangan');

  // Operasi pembagian
  double pembagian = a / b;
  print('$a / $b = $pembagian');
}
